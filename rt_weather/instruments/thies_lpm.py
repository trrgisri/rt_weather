    ##
    # @brief  Parses a one-line LPM telegram given as List and returns a PSVD matrix
    #
    # @param row        List containing elements of a one-line LPM teregram
    # @param dev_addr   Device address consisting of two digits. Default is '00'
    # @return           Tuple of datetime, precipitation rate, and PSVD matrix
    # @retval datetime  Pandas Datetime of the LPM telegram
    # @retval prec_rate Precipitation rate in the past minute [mm/h]
    # @retval psvd      PSVD matrix in a Pandas DataFrame
    #                     with particle diams as Index and velocities as Column.
    @classmethod
    def telegramlist_to_psvd(cls, row, dev_addr='00', dev_time_p=False, timezone='Asia/Tokyo'):
        if len(row) != 521:
            raise ValueError("Bad list len:%d, 521 expected." % len(row))

        # row[0]: "[CPU Date Time] \x02 Device_address"
        if dev_time_p:
            _datetime_curr = pd.to_datetime(row[cls.TELEGRAM_INDEX_DEVICE_DATE]+' '+row[cls.TELEGRAM_INDEX_DEVICE_TIME], format='%d.%m.%y %H:%M:%S').tz_localize(timezone)
        else:
            _cputimestr = WeatherInstrument.convert_control_characters(row[cls.TELEGRAM_INDEX_CPU_DATETIME],'')
            _datetime_curr = pd.to_datetime(_cputimestr, format='[%Y-%m-%d %H:%M:%S.%f] '+dev_addr).tz_localize(timezone)
            #_datetime_curr = pd.to_datetime(row[cls.TELEGRAM_INDEX_CPU_DATETIME], format='[%Y-%m-%d %H:%M:%S.%f] \x0200')
            #_datetime_curr = pd.to_datetime(row[cls.TELEGRAM_INDEX_CPU_DATETIME], format='[%Y-%m-%d %H:%M:%S.%f] \x02'+dev_addr).tz_localize(timezone)

        _df_psvd = pd.DataFrame(index=cls.RAINDROP_VELOCITIES, columns=cls.RAINDROP_DIAMETERS, data=0, dtype=int)
        _df_psvd.index.name = 'Speed'
        _df_psvd.columns.name = 'Diameter'

        _precip_mean_1min = float(row[cls.TELEGRAM_INDEX_1MIN_PRECIPITATION_RATE])
        if _precip_mean_1min > 0.0:  # means precipitation has occured in the past minute
            _ncol = cls.TELEGRAM_INDEX_PRECIPITATION_SPECTRAM  # 1st col of precipitation spectrum
            for _dim in cls.RAINDROP_DIAMETERS:
                _df_psvd[_dim] = [int(r) for r in row[_ncol:_ncol+len(cls.RAINDROP_VELOCITIES)]] # 20 velocity classes
                _ncol += len(cls.RAINDROP_VELOCITIES)
        #else:
        #    return _datetime_curr, 0.0, None

        _pd0, _pd1 = ThiesLPM._calc_pd(_df_psvd.T)
        _mor = float(row[cls.TELEGRAM_INDEX_1MIN_MOR])
        _t5 = math.exp(5.0*math.log(0.05)/_mor) * 100.0
        _dict_tele = {'METAR':row[cls.TELEGRAM_INDEX_1MIN_METAR],
                        'precipitation':_precip_mean_1min,
                        'MOR':_mor,
                        '<1mm':_pd0,
                        '>=1mm':_pd1,
                        'T5':_t5}

        return _datetime_curr, _precip_mean_1min, _df_psvd.T, pd.Series(_dict_tele, name=_datetime_curr)

    @classmethod
    def _calc_pd(cls, df_psvd):
        _sampling_volume = cls.SAMPLING_AREA * cls.SAMPLING_INTERVAL

        _sr_NDi = (df_psvd / cls.RAINDROP_VELOCITIES).sum(axis='columns') / _sampling_volume
        _sr_NDi = _sr_NDi.where(_sr_NDi>0.0, np.nan)
        #print(_sr_NDi)
        #print(_sr_NDi[_sr_NDi.index < 1.0], _sr_NDi[_sr_NDi.index >= 1.0])

        return _sr_NDi[_sr_NDi.index < 1.0].sum(), _sr_NDi[_sr_NDi.index >= 1.0].sum()

