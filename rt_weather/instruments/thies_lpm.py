    @staticmethod
    def load_csvfile(csvpath, dev_addr="00", timezone='Asia/Tokyo'):
        dic_df, dic_prec, _ = ThiesLPM.load_csv_file_into_dataframe(csvpath, dev_addr=dev_addr, timezone=timezone)
        #print(dic_df)
        return DropSizeDistribution(dic_df,
                                    ThiesLPM.SAMPLING_AREA,
                                    ThiesLPM.SAMPLING_INTERVAL,
                                    ThiesLPM.RAINDROP_DIAMETER_BINWIDTHS,
                                    dict_precip=dic_prec,
                                    show_heatmap_p=False)

